#import "Calculator.h"

@implementation Calculator
-(double) add:(double)number1 number2:(double) number2 {
    return number1 + number2;
}

-(double) minus:(double)number1 number2:(double)number2 {
    return number1 - number2;
}

-(double) multiply:(double)number1 number2:(double)number2 {
    return number1 * number2;
}

-(double) divide:(double)number1 number2:(double)number2 {
    return number1 / number2;
}
@end
